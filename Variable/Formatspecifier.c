

#include <stdio.h>

int main()
{

    // int-> %d;

    int num = 40;

    printf("%d", num);

    // float-> %f;

    float decimalValue = 200.3;

    printf("\n%f", decimalValue);

    // char -> %c

    char character = 'p';

    printf("\n%c", character);

    // string ->%s

    char string[30] = "Hi My Name Is Priyanshu";

    printf("\n%s", string);

    return 0;
}
